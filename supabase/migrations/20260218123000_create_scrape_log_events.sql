    id,
    created_at,
    scrape_id,
    career_page_id,
    company_id,
    company_name,
    career_page_url,
    worker,
    event_type,
    severity,
    from_status,
    to_status,
    error_message,
    metrics
FROM ranked_failures
WHERE rn = 1;

CREATE OR REPLACE VIEW scrape_dashboard_live_v AS
WITH status_counts AS (
    SELECT status, COUNT(*)::bigint AS cnt
    FROM scrapes
    GROUP BY status
),
totals AS (
    SELECT COALESCE(SUM(cnt), 0)::bigint AS total_scrapes
    FROM status_counts
),
status_counts_json AS (
    SELECT COALESCE(jsonb_object_agg(status, cnt), '{}'::jsonb) AS status_counts
    FROM status_counts
),
status_percentages_json AS (
    SELECT COALESCE(
        jsonb_object_agg(
            sc.status,
            CASE
                WHEN t.total_scrapes = 0 THEN 0
                ELSE ROUND((sc.cnt::numeric * 100.0) / t.total_scrapes, 2)
            END
        ),
        '{}'::jsonb
    ) AS status_percentages
    FROM status_counts sc
    CROSS JOIN totals t
),
failed_scrapes AS (
    SELECT
        COUNT(*) FILTER (WHERE latest_failed_at >= now() - INTERVAL '24 hours')::bigint AS failed_scrapes_24h,
        COUNT(*)::bigint AS failed_scrapes_all_time
    FROM (
        SELECT scrape_id, MAX(created_at) AS latest_failed_at
        FROM scrape_log_events
        WHERE to_status IN ('failed', 'core_extraction_failed')
           OR event_type = 'scrape_failed'
        GROUP BY scrape_id
    ) f
),
jobs_extracted AS (
    SELECT
        COALESCE(SUM(COALESCE((metrics->>'jobs_cleaned')::bigint, 0)) FILTER (WHERE created_at >= now() - INTERVAL '24 hours'), 0)::bigint AS jobs_extracted_24h,
        COALESCE(SUM(COALESCE((metrics->>'jobs_cleaned')::bigint, 0)), 0)::bigint AS jobs_extracted_all_time
    FROM scrape_log_events
    WHERE event_type = 'jobs_extracted'
),
new_jobs AS (
    SELECT
        COALESCE(SUM(COALESCE((metrics->>'new_jobs')::bigint, 0)) FILTER (WHERE created_at >= now() - INTERVAL '24 hours'), 0)::bigint AS new_jobs_24h,
        COALESCE(SUM(COALESCE((metrics->>'new_jobs')::bigint, 0)), 0)::bigint AS new_jobs_all_time
    FROM scrape_log_events
    WHERE event_type = 'jobs_upserted'
),
websites_hit AS (
    SELECT
        COUNT(DISTINCT metrics->>'url') FILTER (
            WHERE created_at >= now() - INTERVAL '24 hours'
              AND event_type = 'url_hit'
              AND metrics ? 'url'
        )::bigint AS websites_hit_24h,
        COUNT(DISTINCT metrics->>'url') FILTER (
            WHERE event_type = 'url_hit'
              AND metrics ? 'url'
        )::bigint AS websites_hit_all_time
    FROM scrape_log_events
)
SELECT
    t.total_scrapes,
    scj.status_counts,
    spj.status_percentages,
    fs.failed_scrapes_24h,
    fs.failed_scrapes_all_time,
    je.jobs_extracted_24h,
    je.jobs_extracted_all_time,
    nj.new_jobs_24h,
    nj.new_jobs_all_time,
    wh.websites_hit_24h,
    wh.websites_hit_all_time
FROM totals t
CROSS JOIN status_counts_json scj
CROSS JOIN status_percentages_json spj
CROSS JOIN failed_scrapes fs
CROSS JOIN jobs_extracted je
CROSS JOIN new_jobs nj
CROSS JOIN websites_hit wh;

