CREATE OR REPLACE FUNCTION log_scrape_status_transition()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
    IF OLD.status IS DISTINCT FROM NEW.status THEN
        INSERT INTO scrape_log_events (
            scrape_id,
            career_page_id,
            company_id,
            worker,
            event_type,
            severity,
            from_status,
            to_status,
            message,
            metrics
        )
        VALUES (
            NEW.id,
            NEW.career_page_id,
            (SELECT cp.company_id FROM career_pages cp WHERE cp.id = NEW.career_page_id LIMIT 1),
            'system',
            'status_transition',
            'info',
            OLD.status,
            NEW.status,
            'Scrape status changed',
            jsonb_build_object('source', 'scrapes.status trigger')
        );
    END IF;

    RETURN NEW;
END;
$$;

